const fs = require("fs");
const path = require("path");

const picturesDir = path.join(__dirname, "..", "assets", "pictures");
const outputFile = path.join(__dirname, "..", "gallery-images.js");
const imageExtensions = new Set([
  ".avif",
  ".gif",
  ".jpeg",
  ".jpg",
  ".png",
  ".svg",
  ".webp",
]);

const files = fs
  .readdirSync(picturesDir, { withFileTypes: true })
  .filter((entry) => entry.isFile())
  .map((entry) => entry.name)
  .filter((file) => !file.startsWith("."))
  .filter((file) => imageExtensions.has(path.extname(file).toLowerCase()))
  .sort((a, b) => a.localeCompare(b, undefined, { numeric: true, sensitivity: "base" }));

const galleryImages = files.map((file) => ({
  file,
  src: "assets/pictures/" + encodeURIComponent(file),
  caption: path.basename(file, path.extname(file)),
}));

const output =
  "// Generated by scripts/generate-gallery-images.js.\n" +
  "// Edit the image files in assets/pictures/, then rerun the generator.\n" +
  "window.HOB_GALLERY_IMAGES = " +
  JSON.stringify(galleryImages, null, 2) +
  ";\n";

fs.writeFileSync(outputFile, output);
console.log("Generated gallery-images.js with " + galleryImages.length + " images.");
